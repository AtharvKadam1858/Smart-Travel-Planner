import streamlit as st
import pandas as pd

# Load dataset
df = pd.read_csv("smart_travel_maharashtra_full.csv")

st.set_page_config(page_title="Smart Travel Planner", page_icon="ğŸ§³", layout="wide")
st.title("ğŸ§  Smart Travel Planner - Maharashtra Edition")
st.markdown("Plan your next trip with intelligent insights and real user reviews!")

# Sidebar for filtering
st.sidebar.header("ğŸ” Filter Your Preferences")
category = st.sidebar.selectbox("Choose Category", sorted(df['Category'].unique()))
city = st.sidebar.selectbox("Choose City", sorted(df['Location'].unique()))

# Filter data
filtered = df[(df['Category'] == category) & (df['Location'] == city)]

# Group by unique places
grouped = filtered.groupby(['Place_Name', 'Opening_Hours', 'Temperature', 'Weather']).agg({
    'Rating': 'mean',
    'Sentiment': lambda x: (x == 'positive').sum()
}).reset_index()

# Display recommendations
st.subheader(f"ğŸ“ Top {category} Places in {city}")
if grouped.empty:
    st.warning("No results found for your selection.")
else:
    for _, row in grouped.iterrows():
        with st.expander(f"{row['Place_Name']}"):
            st.markdown(f"""
            **ğŸ•’ Opening Hours**: {row['Opening_Hours']}
            **ğŸŒ¡ Temperature**: {row['Temperature']}
            **ğŸŒ¦ Weather**: {row['Weather']}
            **â­ Average Rating**: {round(row['Rating'], 2)}
            **ğŸ‘ Positive Reviews**: {row['Sentiment']}
            """)

# Closing message
st.success("âœ¨ Thank you for using the Smart Travel Planner! âœ¨")